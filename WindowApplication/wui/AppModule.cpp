HINSTANCE AppModule::getInstanceHandle(void) const
{
	return _InstanceHandle;
}

HINSTANCE AppModule::setInstanceHandle(HINSTANCE handle)
{
	HINSTANCE old;


	old = _InstanceHandle;
	_InstanceHandle = handle;

	return _InstanceHandle;
}





/////////////////////////////////////////////////////////////////////////////
//===========================================================================
AppModule* getAppModule(void)
{
	static AppModule instance;

	return &instance;
}





